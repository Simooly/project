#include <stdio.h>

int main() {
	/*
	*******************************
	*  定义注射器型号和容量
	*  syringes : 字符串指针数组，存储三种不同注射器的型号。每个元素都是一个指向字符串常量的指针。
	*  syringe_capacity : 整型数组，存储对应的三种注射器的容量。
	*  "const char * " : 表示指针指向的字符串是不可修改的（即字符串常量）。
	*******************************
	*/ 
    const char *syringes[] = {"1ml", "3ml", "5ml"};
    int syringe_capacity[] = {1, 3, 5};
	
	/*
	*******************************
	*  定义注射剂量选项
	*  doses : 存储五种不同的注射剂量选项的字符串常量。
	*  dose_value : 存储每种注射剂量选项对应的实际剂量值。
	*******************************
	*/ 
    const char *doses[] = {"0.8/1ml", "2/3ml", "3/3ml", "4/5ml", "5/5ml"};
    float dose_value[] = {0.8, 2, 3, 4, 5};

    // 遍历所有可能的组合并输出结果
    for (int i = 0; i < 3; i++) {
    // 外层循环遍历注射器型号和容量，i 从0到2，依次表示第1ml、第3ml和第5ml的注射器。
        for (int j = 0; j < 5; j++) {
    // 中间循环遍历注射剂量选项和它们的值，j 从0到4，表示了五种不同的注射剂量选项。  	
            for (int k = 10; k <= 180; k += 10) {
    // 内层循环遍历注射次数，k 从10到180，每次递增10，表示了注射的次数范围。        	
                float single_dose = dose_value[j] / k;
    // 计算每个组合下的单次注射剂量 single_dose，           
                printf("注射器型号: %s, 单次注射剂量: %.2f ml, 注射剂量选项: %s, 注射次数: %d\n", syringes[i], single_dose, doses[j], k);
            }
        }
    }

    return 0;
}


/*
定义注射器型号和容量：

    首先定义了三种不同型号的注射器和它们对应的容量。这些信息存储在两个数组中，一个数组用于存储注射器型号的字符串常量，另一个数组用于存储对应的容量值。
定义注射剂量选项：

    然后定义了五种不同的注射剂量选项和对应的实际剂量值。同样，这些信息也存储在两个数组中，一个数组存储注射剂量选项的字符串常量，另一个数组存储对应的剂量值。
遍历所有可能的组合并输出结果：

    for循环中，使用三个嵌套的循环来遍历所有可能的组合。外层循环遍历注射器型号和容量，中间循环遍历注射剂量选项和对应的剂量值，内层循环遍历注射次数。
在内部的循环中，计算每个组合下的单次注射剂量，通过将注射剂量值除以注射次数来计算得到。

	使用 printf() 函数将每个组合的信息输出到控制台。

*/
